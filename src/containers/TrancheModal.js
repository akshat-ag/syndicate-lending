const steps = ['Basic Information', 'Add Participating Banks', 'Create Drawdowns'];



class Checkout extends Component {
    constructor(props) {
        super(props);
        this.state = {
            activeStep: 0,
            trancheStartDate: this.props.trancheStartDate,
            trancheEndtDate: this.props.trancheEndtDate,
            trancheAmount: this.props.trancheAmount
        };
        console.log(this.props)
        this.handleEndDate = this.handleEndDate.bind(this);
    }
    
 componentDidMount() {
     //this.setTrancheDate();
 }
  handleNext = () => {
    const { activeStep } = this.state;
    this.setState({
      activeStep: activeStep + 1,
    });
  };

  handleBack = () => {
    const { activeStep } = this.state;
    this.setState({
      activeStep: activeStep - 1,
    });
  };

  handleReset = () => {
    this.setState({
      activeStep: 0,
    });
  };
  handleEndDate = function(e) { 
    this.setState({trancheEndDate: e.target.value});
  }
  getStepContent(step) {
    switch (step) {
      case 0:
        return <BasicInformationTranche 
            amount={this.state.trancheAmount}
            startDate={this.state.trancheStartDate}
            endDate={this.state.trancheEndDate}
            setEndDate={this.handleEndDate}
        />;
      case 1:
        return <div />;
      case 2:
        return <div />;
      default:
        throw new Error('Unknown step');
    }
  }
  render() {
    const { classes } = this.props;
    const { activeStep } = this.state;

    return (
      <React.Fragment>
       
        <main className={classes.layout}>
        
          <Paper className={classes.paper}>
            <Typography variant="title" align="center">
              Add Tranche
            </Typography>
            <Stepper activeStep={activeStep} className={classes.stepper}>
              {steps.map(label => (
                <Step key={label}>
                  <StepLabel>{label}</StepLabel>
                </Step>
              ))}
            </Stepper>
            <React.Fragment>
              {activeStep === steps.length ? (
                <React.Fragment>
                  <Typography variant="headline" gutterBottom>
                    Thank you for your order.
                  </Typography>
                  <Typography variant="subheading">
                    Your order number is #2001539. We have emailed your oder confirmation, and will
                    send you an update when your order has shipped.
                  </Typography>
                </React.Fragment>
              ) : (
                <React.Fragment>
                  {this.getStepContent(activeStep)}
                  <div className={classes.buttons}>
                    {activeStep !== 0 && (
                      <Button onClick={this.handleBack} className={classes.button}>
                        Back
                      </Button>
                    )}
                    <Button
                      variant="contained"
                      color="primary"
                      onClick={this.handleNext}
                      className={classes.button}
                    >
                      {activeStep === steps.length - 1 ? 'Submit' : 'Next'}
                    </Button>
                  </div>
                </React.Fragment>
              )}
            </React.Fragment>
          </Paper>
        </main>
      </React.Fragment>
    );
  }
}

Checkout.propTypes = {
  classes: PropTypes.object.isRequired,
};

export default withStyles(styles)(Checkout);